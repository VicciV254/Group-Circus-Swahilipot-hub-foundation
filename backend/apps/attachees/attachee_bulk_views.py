# ── Toggle Active ─────────────────────────────────────────────────────────────

class ToggleAttacheeActiveView(APIView):
    """POST /attachees/<uuid:pk>/toggle-active/  Body: { "is_active": bool }"""

    ALLOWED_ROLES = {'department_leader', 'system_admin', 'hr_officer', 'executive'}

    def post(self, request, pk):
        caller = request.user
        if caller.role not in self.ALLOWED_ROLES:
            return Response({'detail': 'Permission denied.'}, status=status.HTTP_403_FORBIDDEN)

        try:
            attachee = User.objects.get(pk=pk, role='attachee', organisation=caller.organisation)
        except User.DoesNotExist:
            return Response({'detail': 'Attachee not found.'}, status=status.HTTP_404_NOT_FOUND)

        if caller.role == 'department_leader' and attachee.department != caller.department:
            return Response(
                {'detail': 'Attachee is not in your department.'},
                status=status.HTTP_403_FORBIDDEN,
            )

        new_state = request.data.get('is_active')
        if new_state is None:
            return Response(
                {'detail': 'is_active field is required.'},
                status=status.HTTP_400_BAD_REQUEST,
            )

        attachee.is_active = bool(new_state)
        attachee.save(update_fields=['is_active'])

        action = 'activated' if attachee.is_active else 'deactivated'
        return Response({
            'detail': f'Attachee account {action} successfully.',
            'id': str(attachee.pk),
            'is_active': attachee.is_active,
        })
    