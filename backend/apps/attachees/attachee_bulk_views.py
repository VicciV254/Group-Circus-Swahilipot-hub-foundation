"""
Nexus — Attachee Bulk Import & Management Views
Handles: bulk CSV/Excel import with role-scoped restrictions,
         supervisor assignment/deassignment, account toggle,
         auto-generated passwords, welcome emails.

NOTE: employee_id is auto-generated by a DB-level trigger
(generate_employee_id()) based on NEW.is_staff, using the
employee_id_seq sequence. Do NOT set employee_id in Python —
leave it out of create_user() calls entirely.
"""
